    {
        cString = [cString substringFromIndex:2];
    }
    //如果是#开头的，那么截取字符串，字符串从索引为1的位置开始，一直到末尾
    if ([cString hasPrefix:@"#"])
    {
        cString = [cString substringFromIndex:1];
    }
    NSString *alphaString = @"";
    if (cString.length == 8) {
        
        alphaString = [NSString stringWithFormat:@"%@",[cString substringToIndex:(cString.length - 6)]];
        cString = [cString substringFromIndex:2];
    }
    
    unsigned int number;
    [[NSScanner scannerWithString:alphaString] scanHexInt:&number];
    float alpha = number / 255.0;
    
    if ([cString length] != 6)
    {
        return [UIColor clearColor];
    }
    
    // Separate into r, g, b substrings
    NSRange range;
    range.location = 0;
    range.length = 2;
    //r
    NSString *rString = [cString substringWithRange:range];
